

public class Pixel{
	//--------------------------------------------------------------------------------------
	//										ATRIBUTOS
	//--------------------------------------------------------------------------------------
	private int linha;
	private int coluna;
	private int cor;
	private int rotulo;


	//--------------------------------------------------------------------------------------
	//										CONSTRUTOR
	//--------------------------------------------------------------------------------------
	public Pixel(int linha, int coluna, int cor, int rotulo) {		
		this.linha = linha;
		this.coluna = coluna;
		this.cor = cor;
		this.rotulo = rotulo;
	}	
	
	//--------------------------------------------------------------------------------------
	//										METODOS
	//--------------------------------------------------------------------------------------
	public int getLinha() {
		return linha;
	}
	public int getColuna() {
		return coluna;
	}
	public int getCor() {
		return cor;
	}
	public int getRotulo() {
		return rotulo;
	}
	public void setRotulo(int rotulo) {
		this.rotulo = rotulo;
	}

	public void setCor(int cor) {
		this.cor = cor;		
	}

	public void setColuna(int coluna) {
		this.coluna = coluna;
	}

	public void setLinha(int linha) {
		this.linha = linha;
	}


}
